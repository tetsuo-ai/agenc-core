/**
 * Lean message-helper surface used by `runtime/src/session/**`.
 *
 * Mirrors the small slice of the upstream agenc `utils/messages.ts`
 * API that the gut session subsystem actually reaches for. Operates on
 * the duck-typed message shape `{ type, message: { role, content }, ... }`,
 * matching the AgenC wire format that AgenC adapters consume.
 *
 * Extends the local createUserMessage helper to accept a
 * caller-provided `timestamp` (the AgenC compact command uses it to keep the
 * retained slash-stdout newest in the post-compact tail).
 */

import { randomUUID } from "node:crypto";

const LOCAL_COMMAND_CAVEAT_TAG = "local-command-caveat";
const COMMAND_NAME_TAG = "command-name";
const COMMAND_MESSAGE_TAG = "command-message";
const COMMAND_ARGS_TAG = "command-args";

export interface UserMessageInput {
  readonly content: unknown;
  readonly isMeta?: boolean;
  readonly isVisibleInTranscriptOnly?: boolean;
  readonly isVirtual?: boolean;
  readonly isCompactSummary?: boolean;
  readonly summarizeMetadata?: unknown;
  readonly uuid?: string;
  readonly timestamp?: string;
}

export interface UserMessage {
  type: "user";
  uuid: string;
  timestamp: string;
  message: { role: "user"; content: unknown };
  isMeta: boolean;
  isVisibleInTranscriptOnly: boolean;
  isVirtual: boolean;
  isCompactSummary: boolean;
  summarizeMetadata?: unknown;
}

export function createUserMessage(input: UserMessageInput): UserMessage {
  return {
    type: "user",
    uuid: input.uuid ?? randomUUID(),
    timestamp: input.timestamp ?? new Date().toISOString(),
    message: { role: "user", content: input.content },
    isMeta: input.isMeta ?? false,
    isVisibleInTranscriptOnly: input.isVisibleInTranscriptOnly ?? false,
    isVirtual: input.isVirtual ?? false,
    isCompactSummary: input.isCompactSummary ?? false,
    ...(input.summarizeMetadata !== undefined
      ? { summarizeMetadata: input.summarizeMetadata }
      : {}),
  };
}

/**
 * Builds the synthetic caveat that wraps local-command output so the
 * model treats it as out-of-band context rather than user input.
 */
export function createSyntheticUserCaveatMessage(): UserMessage {
  return createUserMessage({
    content: `<${LOCAL_COMMAND_CAVEAT_TAG}>Caveat: The messages below were generated by the user while running local commands. DO NOT respond to these messages or otherwise consider them in your response unless the user explicitly asks you to.</${LOCAL_COMMAND_CAVEAT_TAG}>`,
    isMeta: true,
  });
}

/**
 * Formats the command-input breadcrumb the model sees when a slash
 * command runs.
 */
export function formatCommandInputTags(
  commandName: string,
  args: string,
): string {
  return `<${COMMAND_NAME_TAG}>/${commandName}</${COMMAND_NAME_TAG}>
            <${COMMAND_MESSAGE_TAG}>${commandName}</${COMMAND_MESSAGE_TAG}>
            <${COMMAND_ARGS_TAG}>${args}</${COMMAND_ARGS_TAG}>`;
}

interface MessageLike {
  readonly type?: string;
  readonly subtype?: string;
}

function isCompactBoundaryMessage(
  message: MessageLike | null | undefined,
): boolean {
  if (!message) return false;
  return message.type === "system" && message.subtype === "compact_boundary";
}

export function getMessagesAfterCompactBoundary<T extends MessageLike>(
  messages: ReadonlyArray<T>,
): T[] {
  for (let i = messages.length - 1; i >= 0; i -= 1) {
    if (isCompactBoundaryMessage(messages[i]!)) {
      return messages.slice(i + 1) as T[];
    }
  }
  return [...messages];
}
