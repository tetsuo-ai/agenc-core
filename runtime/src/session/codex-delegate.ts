/**
 * Build the review-scoped `TurnContext` from the parent context +
 * reviewer overrides. Mirrors the inline turn-context assembly at
 * upstream `codex-rs/core/src/session/review.rs:101-146`.
 *
 * Unlike upstream's hand-built `TurnContext { ... }` struct literal,
 * gut threads the overrides through `buildTurnContext` so the frozen-
 * config invariant + metadata state get the same treatment as every
 * other turn. The caller-visible difference: reviewer web/view-image
 * features are disabled (features call is inert because gut's
 * `ManagedFeatures` is functions-only вЂ” see RESERVED comment below),
 * and the reviewer system prompt is applied at call-site (the
 * delegate prepends a system message before the user prompt).
 */
export function buildReviewTurnContext(
  parentCtx: DelegateTurnContextLike,
  reviewerModel: string,
  reviewerModelInfo: ModelInfo | undefined,
  reviewSubId: string,
): TurnContext {
  const modelInfo: ModelInfo = reviewerModelInfo ?? {
    ...parentCtx.modelInfo,
    slug: reviewerModel,
  };
  // Reviewer-scoped SessionConfiguration derived from parent. The
  // reviewer must not surface the parent's user/developer
  // instructions (those are the author's context, not the reviewer's)
  // and must carry the reviewer-scoped `collaborationMode.model` +
  // review-forced `approvalPolicy: never` / `sandboxPolicy:
  // read_only`.
  const sc = {
    cwd: parentCtx.cwd,
    approvalPolicy: { value: "never" as const },
    sandboxPolicy: { value: "read_only" as const },
    fileSystemSandboxPolicy: parentCtx.fileSystemSandboxPolicy,
    networkSandboxPolicy: parentCtx.networkSandboxPolicy,
    windowsSandboxLevel: parentCtx.windowsSandboxLevel,
    collaborationMode: {
      model: reviewerModel,
      ...(parentCtx.collaborationMode.reasoningEffort !== undefined
        ? { reasoningEffort: parentCtx.collaborationMode.reasoningEffort }
        : {}),
    },
    // Upstream zeroes developer_instructions / user_instructions for
    // the reviewer (session/review.rs:121-122). Preserve that contract.
    dynamicTools: parentCtx.dynamicTools,
    sessionSource: parentCtx.sessionSource,
  };
  return buildTurnContext({
    conversationId: parentCtx.turnMetadataState.conversationId,
    subId: reviewSubId,
    config: parentCtx.config,
    modelInfo,
    provider: parentCtx.provider,
    sessionConfiguration: sc,
    ...(parentCtx.authManager !== undefined
      ? { authManager: parentCtx.authManager }
      : {}),
    ...(parentCtx.environment !== undefined
      ? { environment: parentCtx.environment }
      : {}),
    ...(parentCtx.network !== undefined ? { network: parentCtx.network } : {}),
    jsRepl: parentCtx.jsRepl,
  });
}

/**
 * Compose a minimal review-scoped message list for the provider call.
 * Upstream hands `input: Vec<UserInput>` to the child codex and lets
 * the child session's prompt builder prepend its `base_instructions`
 * (the reviewer system prompt). Because gut's delegate is a flat
 * one-shot (no child session), the prompt construction happens here.
 *
 * Layout:
 *   [0] system  вЂ” reviewer system prompt
 *   [1..]       вЂ” caller-supplied `input` messages verbatim
 *
 * The caller's messages pass through unchanged so a reviewer-prompt
 * that already contains a `user` message with the diff body stays
 * intact.
 */
function composeReviewerMessages(
  systemPrompt: string,
  input: ReadonlyArray<LLMMessage>,
): LLMMessage[] {
  const systemMessage: LLMMessage = {
    role: "system",
    content: systemPrompt,
  };
  return [systemMessage, ...input];
}

/**
 * Upstream codex `run_codex_thread_one_shot`
 * (`codex_delegate.rs:156-237`) port. Runs a bounded single-turn
 * review provider call under the review-scoped `TurnContext` and
 * returns a structured outcome.
 *
 * Upstream sequence preserved:
 *   1. Derive a child cancellation context from the caller's signal
 *      so shutdown can fire without touching the caller's controller.
 *   2. Register the turn as `kind: "review"` through `spawnTask` so
 *      the Wave 2 replace-on-new-turn / abort-cascade contract
 *      applies.
 *   3. Synthesize the review-scoped `TurnContext` + reviewer system
 *      prompt, compose the provider input, and issue a single
 *      provider call.
 *   4. Parse the assistant text into a `ReviewOutput` (mirrors
 *      upstream `parse_review_output_event`) and classify the
 *      verdict.
 *   5. Emit `exit_review_mode` once on termination (happy, timeout,
 *      aborted).
 *   6. Drain the task via `onTaskFinished` so the session's active
 *      turn slot clears.
 *
 * The outcome shape matches `CodexThreadOneShotOutcome` вЂ” never
 * throws on review failure paths (timeout, abort, malformed
 * response). Propagates a typed {@link ReviewerModelMismatchError}
 * up-front when the reviewer model is not resolvable through the
 * provider, because that is a setup-time contract violation, not a
 * review outcome.
 */
export async function runOneShot(
  session: DelegateSessionLike,
  req: CodexThreadOneShotRequest,
): Promise<CodexThreadOneShotOutcome> {
  // Reviewer-model resolution. Upstream consults ModelsManager; gut
  // delegates the check to the provider's capability hint (the
  // provider exposes `getExecutionProfile` but not a model-list
  // lookup, so the delegate accepts anything the caller specifies
  // and trusts the provider to reject at the adapter boundary). The
  // typed mismatch error exists so a future ModelsManager port can
  // flip the trust model without changing the public surface.
  const reviewerModel =
    req.reviewerModel ?? req.parentContext.modelInfo.slug;
  if (reviewerModel.length === 0) {
    throw new ReviewerModelMismatchError(reviewerModel, session.provider.name);
  }

  // Parent abort в†’ child controller merge. Fire the child when the
  // parent fires; tear down the listener on completion.
  const childController = new AbortController();
  const parentSignal = req.signal;
  let parentAbortListener: (() => void) | undefined;
  if (parentSignal) {
    if (parentSignal.aborted) {
      childController.abort(parentSignal.reason);
    } else {
      parentAbortListener = () => {
        childController.abort(parentSignal.reason);
      };
      parentSignal.addEventListener("abort", parentAbortListener, {
        once: true,
      });
    }
  }

  // Upstream: `sess.spawn_task(tc, input, ReviewTask::new())` registers
  // the review turn with `TaskKind::Review`. Gut's `spawnTask` returns
  // the task handle; we pass our child controller so abort flows
  // through the same cancellation surface.
  const task = await session.spawnTask({
    subId: req.subId,
    kind: "review",
    abortController: childController,
    startedAtMs: Date.now(),
  });

  // Timeout guard. Upstream applies deadlines at the manager level
  // (`run_before_review_deadline`, review_session.rs:899-914); gut
  // exposes the budget on the delegate too for callers without a
  // manager. On expiry, fire the child controller and record the
  // timeout reason so the final outcome classifies correctly.
  let timeoutFired = false;
  let timeoutHandle: ReturnType<typeof setTimeout> | undefined;
  if (req.timeoutMs !== undefined && req.timeoutMs > 0) {
    timeoutHandle = setTimeout(() => {
      timeoutFired = true;
      childController.abort("timeout");
    }, req.timeoutMs);
  }

  let assistantText: string | null = null;
  let providerError: Error | null = null;
  try {
    // Build the review-scoped TurnContext so the provider call runs
    // under the reviewer-facing model/auth envelope. The context is
    // built but consumed implicitly вЂ” gut's provider interface takes
    // (messages, options) rather than (ctx, messages), so the ctx's
    // role here is to carry the frozen review-scoped config for the
    // runtime to observe (emitted events, telemetry, future turn-
    // state writes). The provider call below reads `modelInfo.slug`
    // from this ctx when the reviewer model differs from the parent.
    void buildReviewTurnContext(
      req.parentContext,
      reviewerModel,
      req.reviewerModelInfo,
      req.subId,
    );

    const messages = composeReviewerMessages(
      REVIEW_SYSTEM_PROMPT,
      req.input,
    );

    if (childController.signal.aborted) {
      // Aborted before we even issued the provider call (parent
      // signal fired synchronously, or timeout is zero and fired on
      // the microtask queue). Fall through to the termination block
      // with no assistantText.
    } else {
      const response: LLMResponse = await session.provider.chat(
        messages as LLMMessage[],
        {
          signal: childController.signal,
          ...(req.finalOutputJsonSchema !== undefined
            ? {
                structuredOutput: {
                  schema: req.finalOutputJsonSchema,
                } as unknown as import("../llm/types.js").LLMStructuredOutputRequest,
              }
            : {}),
        },
      );
      assistantText = response.content ?? null;
    }
  } catch (err) {
    providerError = err instanceof Error ? err : new Error(String(err));
  } finally {
    if (timeoutHandle !== undefined) clearTimeout(timeoutHandle);
    if (parentSignal && parentAbortListener !== undefined) {
      parentSignal.removeEventListener("abort", parentAbortListener);
    }
  }

  // Verdict classification. Order matches upstream: timeout в†’ abort
  // в†’ provider error в†’ parsed output. Timeout wins over generic
  // abort so the UI can surface the more-specific reason.
  let verdict: CodexThreadOneShotOutcome["verdict"];
  let output: ReviewOutput;
  let error: Error | null = null;
  if (timeoutFired) {
    verdict = "timeout";
    output = emptyReviewOutput();
    error = new Error("review timed out");
  } else if (childController.signal.aborted && assistantText === null) {
    verdict = "aborted";
    output = emptyReviewOutput();
    const reason = childController.signal.reason;
    error =
      reason instanceof Error
        ? reason
        : new Error(
            typeof reason === "string" && reason.length > 0
              ? reason
              : "aborted",
          );
  } else if (providerError !== null && assistantText === null) {
    // Provider failed without returning any content. Classify as a
    // "fail" verdict (the review could not complete), surface the
    // typed error in `error` so the caller can telemetry-route.
    verdict = "fail";
    output = emptyReviewOutput();
    error = providerError;
  } else {
    output = assistantText !== null ? parseReviewOutput(assistantText) : emptyReviewOutput();
    if (output.findings.length > 0) {
      verdict = "fail"; // findings present в†’ reviewer flagged issues
    } else if (output.overallExplanation.trim().length === 0) {
      verdict = "partial"; // nothing useful came back
    } else {
      verdict = "pass"; // reviewer had something to say but no findings
    }
  }

  // Emit the `exit_review_mode` event on every termination path so
  // consumers do not have to reconcile "did the task emit the event
  // or did I need to emit it myself". Upstream emits from
  // `tasks/review.rs::exit_review_mode` (`tasks/review.rs:213-283`)
  // including the rollout record; gut emits only the event here and
  // leaves rollout materialization to a later port (the RESERVED
  // record_conversation_items + record_response_item_and_emit_turn_item
  // call sites).
  const exitPayload: ExitReviewModePayload = {
    subId: req.subId,
    reason:
      verdict === "timeout"
        ? "timeout"
        : verdict === "aborted"
          ? "aborted"
          : "completed",
    reviewOutput: output,
    modelUsed: reviewerModel,
    request: req.request,
  };
  session.sendEvent(req.subId, {
    type: "exit_review_mode",
    payload: exitPayload,
  });

  // Drain the task from the session registry. Upstream
  // `SessionTaskContext::on_task_finished` fires after `ReviewTask::run`
  // returns; gut's `Session.onTaskFinished` clears the activeTurn
  // slot when the task registry empties. `void`-await is fine because
  // the lifecycle awaits `task.done` elsewhere (graceful interruption
  // path); we resolve `done` here by explicitly finishing.
  await session.onTaskFinished(task.subId);

  return {
    verdict,
    output,
    rawText: assistantText,
    modelUsed: reviewerModel,
    error,
  };
}

