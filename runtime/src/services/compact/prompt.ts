const NO_TOOLS_TRAILER =
  "\n\nREMINDER: Do NOT call any tools. Respond with plain text only - " +
  "an <analysis> block followed by a <summary> block. " +
  "Tool calls will be rejected and you will fail the task.";

const ANALYSIS_BLOCK_PATTERN = /<analysis>[\s\S]*?<\/analysis>/gu;
const SUMMARY_BLOCK_PATTERN = /<summary>([\s\S]*?)<\/summary>/u;

export function getPartialCompactPrompt(
  customInstructions?: string,
  direction: PartialCompactDirection = "from",
): string {
  const template =
    direction === "up_to"
      ? PARTIAL_COMPACT_UP_TO_PROMPT
      : PARTIAL_COMPACT_PROMPT;
  return appendInstructionsAndTrailer(
    NO_TOOLS_PREAMBLE + template,
    customInstructions,
  );
}

export function getCompactPrompt(customInstructions?: string): string {
  return appendInstructionsAndTrailer(
    NO_TOOLS_PREAMBLE + BASE_COMPACT_PROMPT,
    customInstructions,
  );
}

export function stripAnalysisTags(text: string): string {
  return text.replace(ANALYSIS_BLOCK_PATTERN, "").trim();
}

export function formatCompactSummary(summary: string): string {
  let formattedSummary = stripAnalysisTags(summary);
  const summaryMatch = formattedSummary.match(SUMMARY_BLOCK_PATTERN);

  if (summaryMatch) {
    const content = summaryMatch[1] ?? "";
    // Use a function replacer: a string replacement would treat $-sequences in
    // the model-authored summary ($&, $1, $$, shell snippets like $@) as
    // special substitutions and silently corrupt the output. Function return
    // values are inserted literally.
    const replacement = `Summary:\n${content.trim()}`;
    formattedSummary = formattedSummary.replace(
      SUMMARY_BLOCK_PATTERN,
      () => replacement,
    );
  }

  return formattedSummary.replace(/\n\n+/gu, "\n\n").trim();
}

export function getCompactUserSummaryMessage(
  summary: string,
  suppressFollowUpQuestions?: boolean,
  transcriptPath?: string,
  recentMessagesPreserved?: boolean,
): string {
  const formattedSummary = formatCompactSummary(summary);

  let baseSummary = `This session is being continued from a previous conversation that ran out of context. The summary below covers the earlier portion of the conversation.

${formattedSummary}`;

  if (transcriptPath) {
    baseSummary += `\n\nIf you need specific details from before compaction (like exact code snippets, error messages, or content you generated), read the full transcript at: ${transcriptPath}`;
  }

  if (recentMessagesPreserved) {
    baseSummary += "\n\nRecent messages are preserved verbatim.";
  }

  if (!suppressFollowUpQuestions) return baseSummary;

  return `${baseSummary}
Continue the conversation from where it left off without asking the user any further questions. Resume directly - do not acknowledge the summary, do not recap what was happening, do not preface with "I'll continue" or similar. Pick up the last task as if the break never happened.`;
}

function appendInstructionsAndTrailer(
  prompt: string,
  customInstructions?: string,
): string {
  let output = prompt;

  if (customInstructions && customInstructions.trim() !== "") {
    output += `\n\nAdditional Instructions:\n${customInstructions}`;
  }

  return output + NO_TOOLS_TRAILER;
}
