/**
 * `agenc update` — self-update the runtime from the public release manifest.
 *
 *   agenc update                     install the latest released runtime
 *   agenc update --check             report whether an update exists; no writes
 *   agenc update --pin <x.y.z>       install a specific release instead of latest
 *
 * Speaks the exact install contract shared by scripts/install/install.sh and
 * the npm launcher's runtime-manager (packages/agenc/lib/runtime-manager.mjs):
 * download the platform tarball named in agenc-runtime-manifest.json, verify
 * its sha256, extract under <AGENC_HOME>/runtime/<version>/, record the sha in
 * the .agenc-runtime-ok marker, then repoint the generated shell wrapper at
 * the new runtime. Old runtime trees stay in place so a running daemon keeps
 * working until it is restarted.
 *
 * npm-launcher installs pin their runtime through the manifest bundled into
 * @tetsuo-ai/agenc, so there is no wrapper to rewrite — update detects that
 * and prints the `npm install -g` path instead of downloading anything.
 *
 * Trust boundary: the manifest and tarball are remote release data. Nothing
 * downloaded is executed during the update; the tarball is only extracted
 * after its sha256 matches the manifest, and only wrappers carrying the
 * install.sh generation signature are ever rewritten.
 */

import { spawnSync, type SpawnSyncReturns } from "node:child_process";
import { createHash } from "node:crypto";
import {
  chmodSync,
  createReadStream,
  createWriteStream,
  existsSync,
  mkdirSync,
  readFileSync,
  renameSync,
  rmSync,
  statSync,
  writeFileSync,
} from "node:fs";
import { homedir, tmpdir } from "node:os";
import { gt as semverGt } from "../utils/semver.js";
import { delimiter, join } from "node:path";
import { Readable } from "node:stream";
import { pipeline } from "node:stream/promises";
import { fileURLToPath } from "node:url";
import { resolveAgencHome } from "../config/env.js";
import { VERSION } from "../version.js";

export const DEFAULT_RELEASE_REPO = "tetsuo-ai/agenc-releases";
const WRAPPER_SIGNATURE = "Generated by AgenC install.sh";
const RUNTIME_MARKER = ".agenc-runtime-ok";

export type AgenCUpdateCliCommand =
  | {
      readonly kind: "update";
      readonly check: boolean;
      readonly json: boolean;
      readonly pinVersion?: string;
      readonly manifestUrl?: string;
      readonly repo?: string;
      readonly wrapper?: string;
    }
  | { readonly kind: "help"; readonly text: string }
  | { readonly kind: "error"; readonly message: string };

export function formatAgenCUpdateCliHelpText(): string {
  return [
    "agenc update — update the AgenC runtime to the latest public release",
    "",
    "Usage:",
    "  agenc update                    Download, verify, and install the latest runtime",
    "  agenc update --check            Report whether an update is available (no writes)",
    "  agenc update --pin <x.y.z>      Install a specific release instead of latest",
    "",
    "Options:",
    "  --check               Check only; never downloads or writes",
    "  --json                Machine-readable result on stdout",
    "  --pin <x.y.z>         Pin a release version",
    "  --repo <owner/name>   Release repository (default: " +
      DEFAULT_RELEASE_REPO +
      ")",
    "  --manifest-url <url>  Manifest override (supports file:// for testing)",
    "  --wrapper <path>      Explicit wrapper script to repoint",
    "  -h, --help            Show this help text",
    "",
    "The new runtime installs side by side under <AGENC_HOME>/runtime/<version>/",
    "with sha256 verification, then the `agenc` wrapper generated by install.sh",
    "is repointed. A running daemon keeps the old version until restarted:",
    "  agenc daemon restart",
    "",
    "npm-launcher installs (`npm install -g @tetsuo-ai/agenc`) update with:",
    "  npm install -g @tetsuo-ai/agenc@latest",
    "",
    "Examples:",
    "  agenc update --check",
    "  agenc update",
  ].join("\n");
}

function takeFlagValue(
  rest: readonly string[],
  index: number,
  flag: string,
): { value: string } | { error: string } {
  const value = rest[index + 1];
  if (value === undefined || value.startsWith("-")) {
    return { error: `${flag} needs a value` };
  }
  return { value };
}

export function parseAgenCUpdateCliArgs(
  argv: readonly string[],
): AgenCUpdateCliCommand | null {
  if (argv[0] !== "update") return null;
  const rest = argv.slice(1);
  let check = false;
  let json = false;
  let pinVersion: string | undefined;
  let manifestUrl: string | undefined;
  let repo: string | undefined;
  let wrapper: string | undefined;
  for (let i = 0; i < rest.length; i += 1) {
    const arg = rest[i];
    if (arg === "-h" || arg === "--help") {
      return { kind: "help", text: formatAgenCUpdateCliHelpText() };
    }
    if (arg === "--check") {
      check = true;
      continue;
    }
    if (arg === "--json") {
      json = true;
      continue;
    }
    if (arg === "--pin" || arg === "--manifest-url" || arg === "--repo" || arg === "--wrapper") {
      const taken = takeFlagValue(rest, i, arg);
      if ("error" in taken) return { kind: "error", message: taken.error };
      if (arg === "--pin") pinVersion = taken.value;
      else if (arg === "--manifest-url") manifestUrl = taken.value;
      else if (arg === "--repo") repo = taken.value;
      else wrapper = taken.value;
      i += 1;
      continue;
    }
    return {
      kind: "error",
      message: `unknown update option '${arg}' (see 'agenc help update')`,
    };
  }
  if (pinVersion !== undefined && !/^\d+\.\d+\.\d+(-[0-9A-Za-z.-]+)?$/.test(pinVersion)) {
    return { kind: "error", message: `--pin expects a semver version, got '${pinVersion}'` };
  }
  return {
    kind: "update",
    check,
    json,
    ...(pinVersion !== undefined ? { pinVersion } : {}),
    ...(manifestUrl !== undefined ? { manifestUrl } : {}),
    ...(repo !== undefined ? { repo } : {}),
    ...(wrapper !== undefined ? { wrapper } : {}),
  };
}

// --- manifest -----------------------------------------------------------------

export interface RuntimeManifestArtifact {
  readonly platform: string;
  readonly arch: string;
  readonly url: string;
  readonly sha256: string;
  readonly bins?: { readonly agenc?: string };
}

export interface RuntimeManifest {
  readonly runtimeVersion: string;
  readonly artifacts: readonly RuntimeManifestArtifact[];
}

export function resolveUpdateManifestUrl(options: {
  readonly pinVersion?: string;
  readonly manifestUrl?: string;
  readonly repo?: string;
  readonly env?: Readonly<Record<string, string | undefined>>;
}): string {
  const env = options.env ?? process.env;
  if (options.manifestUrl !== undefined) return options.manifestUrl;
  const envUrl = env.AGENC_INSTALL_MANIFEST_URL?.trim();
  if (envUrl !== undefined && envUrl.length > 0) return envUrl;
  const repo =
    options.repo ?? env.AGENC_INSTALL_REPO?.trim() ?? DEFAULT_RELEASE_REPO;
  if (options.pinVersion !== undefined) {
    return `https://github.com/${repo}/releases/download/agenc-v${options.pinVersion}/agenc-runtime-manifest.json`;
  }
  return `https://github.com/${repo}/releases/latest/download/agenc-runtime-manifest.json`;
}

export function updatePlatformSlug(
  platform: NodeJS.Platform = process.platform,
  arch: string = process.arch,
): { os: string; arch: string } {
  return { os: platform === "win32" ? "win" : platform, arch };
}

async function fetchToFile(
  url: string,
  dest: string,
  fetchImpl: typeof fetch,
): Promise<void> {
  if (url.startsWith("file://")) {
    await pipeline(createReadStream(fileURLToPath(url)), createWriteStream(dest));
    return;
  }
  if (!url.startsWith("https://")) {
    throw new Error(`refusing non-https artifact URL: ${url}`);
  }
  const res = await fetchImpl(url, { redirect: "follow" });
  if (!res.ok || res.body === null) {
    throw new Error(`download failed ${res.status} ${res.statusText}: ${url}`);
  }
  await pipeline(
    Readable.fromWeb(res.body as import("node:stream/web").ReadableStream),
    createWriteStream(dest),
  );
}

export async function fetchRuntimeManifest(
  url: string,
  fetchImpl: typeof fetch = globalThis.fetch,
): Promise<RuntimeManifest> {
  let raw: string;
  if (url.startsWith("file://")) {
    raw = readFileSync(fileURLToPath(url), "utf8");
  } else {
    if (!url.startsWith("https://")) {
      throw new Error(`refusing non-https manifest URL: ${url}`);
    }
    const res = await fetchImpl(url, { redirect: "follow" });
    if (!res.ok) {
      throw new Error(`manifest fetch failed ${res.status} ${res.statusText}: ${url}`);
    }
    raw = await res.text();
  }
  const parsed: unknown = JSON.parse(raw);
  if (
    typeof parsed !== "object" ||
    parsed === null ||
    typeof (parsed as { runtimeVersion?: unknown }).runtimeVersion !== "string" ||
    !Array.isArray((parsed as { artifacts?: unknown }).artifacts)
  ) {
    throw new Error("manifest is missing runtimeVersion/artifacts");
  }
  return parsed as RuntimeManifest;
}

export function selectUpdateArtifact(
  manifest: RuntimeManifest,
  slug: { os: string; arch: string } = updatePlatformSlug(),
): RuntimeManifestArtifact {
  const match = manifest.artifacts.find(
    (a) => a.platform === slug.os && a.arch === slug.arch,
  );
  if (match === undefined) {
    const have = manifest.artifacts
      .map((a) => `${a.platform}-${a.arch}`)
      .join(", ");
    throw new Error(
      `no runtime build for ${slug.os}-${slug.arch} (available: ${have || "none"})`,
    );
  }
  if (!/^[0-9a-f]{64}$/.test(match.sha256 ?? "")) {
    throw new Error("manifest artifact is missing a valid sha256");
  }
  if (typeof match.url !== "string" || match.url.length === 0) {
    throw new Error("manifest artifact is missing a url");
  }
  return match;
}

// --- wrapper discovery ----------------------------------------------------------

export interface InstallShWrapper {
  readonly path: string;
  readonly nodeBin: string;
  readonly runtimeBin: string;
  readonly agencHome: string;
}

/**
 * Parse a wrapper generated by scripts/install/install.sh. Only files carrying
 * the generation signature are eligible — `agenc update` must never rewrite a
 * user-authored script or the npm launcher shim that happens to be named
 * `agenc`.
 */
export function parseInstallShWrapper(path: string): InstallShWrapper | null {
  let content: string;
  try {
    if (!statSync(path).isFile()) return null;
    content = readFileSync(path, "utf8");
  } catch {
    return null;
  }
  if (!content.includes(WRAPPER_SIGNATURE)) return null;
  const homeMatch = content.match(
    /export AGENC_HOME="\$\{AGENC_HOME:-([^}"]+)\}"/,
  );
  const execMatch = content.match(/exec "([^"]+)" "([^"]+)" "\$@"/);
  if (homeMatch === null || execMatch === null) return null;
  return {
    path,
    nodeBin: execMatch[1],
    runtimeBin: execMatch[2],
    agencHome: homeMatch[1],
  };
}

export function findInstallShWrappers(options: {
  readonly env?: Readonly<Record<string, string | undefined>>;
  readonly userHome?: string;
}): InstallShWrapper[] {
  const env = options.env ?? process.env;
  const userHome = options.userHome ?? homedir();
  const dirs = new Set<string>();
  for (const dir of (env.PATH ?? "").split(delimiter)) {
    if (dir.length > 0) dirs.add(dir);
  }
  dirs.add(join(userHome, ".local", "bin"));
  const found: InstallShWrapper[] = [];
  for (const dir of dirs) {
    const candidate = join(dir, "agenc");
    const wrapper = parseInstallShWrapper(candidate);
    if (wrapper !== null) found.push(wrapper);
  }
  return found;
}

export function renderInstallShWrapper(wrapper: {
  readonly nodeBin: string;
  readonly runtimeBin: string;
  readonly agencHome: string;
}): string {
  return [
    "#!/bin/sh",
    `# ${WRAPPER_SIGNATURE} — rewritten on every install/upgrade.`,
    `export AGENC_HOME="\${AGENC_HOME:-${wrapper.agencHome}}"`,
    `exec "${wrapper.nodeBin}" "${wrapper.runtimeBin}" "$@"`,
    "",
  ].join("\n");
}

function rewriteWrapper(wrapper: InstallShWrapper, runtimeBin: string): void {
  const tmp = `${wrapper.path}.agenc-update-${process.pid}`;
  writeFileSync(tmp, renderInstallShWrapper({ ...wrapper, runtimeBin }));
  chmodSync(tmp, 0o755);
  renameSync(tmp, wrapper.path);
}

// --- runtime install (marker contract) -------------------------------------------

export interface InstallRuntimeResult {
  readonly binPath: string;
  readonly downloaded: boolean;
}

/**
 * Validate the result of the `tar` extraction. When `tar` is not on PATH,
 * spawnSync never runs the process: it sets `res.error` (ENOENT) and leaves
 * `res.status` null, which the old `status !== 0` check reported as the opaque
 * "tar extraction failed (status null)". Surface a clear, actionable message
 * for the missing-binary case, and keep the extraction-failed message for a
 * process that ran and exited non-zero.
 */
export function assertTarExtractionSucceeded(
  res: Pick<SpawnSyncReturns<Buffer>, "error" | "status" | "signal" | "stderr">,
): void {
  if (res.error) {
    if ((res.error as NodeJS.ErrnoException).code === "ENOENT") {
      throw new Error(
        "tar not found on PATH — install tar (GNU or BSD) to extract the runtime",
      );
    }
    throw new Error(`failed to run tar: ${res.error.message}`);
  }
  if (res.status !== 0) {
    throw new Error(
      `tar extraction failed (status ${res.status ?? res.signal}): ${res.stderr?.toString() ?? ""}`,
    );
  }
}

export async function installRuntimeFromManifest(options: {
  readonly manifest: RuntimeManifest;
  readonly artifact: RuntimeManifestArtifact;
  readonly agencHome: string;
  readonly fetchImpl?: typeof fetch;
  readonly tmpDir?: string;
}): Promise<InstallRuntimeResult> {
  const { manifest, artifact, agencHome } = options;
  const version = manifest.runtimeVersion;
  const installDir = join(agencHome, "runtime", version);
  const binRel = artifact.bins?.agenc ?? "node_modules/@tetsuo-ai/runtime/bin/agenc";
  const binPath = join(installDir, binRel);
  const marker = join(installDir, RUNTIME_MARKER);

  if (existsSync(marker)) {
    try {
      if (readFileSync(marker, "utf8").trim() === artifact.sha256) {
        return { binPath, downloaded: false };
      }
    } catch {
      // fall through to a fresh download
    }
  }

  const tmp = join(
    options.tmpDir ?? tmpdir(),
    `agenc-update-${version}-${process.pid}.tar.gz`,
  );
  try {
    await fetchToFile(artifact.url, tmp, options.fetchImpl ?? globalThis.fetch);
    const hash = createHash("sha256");
    await pipeline(createReadStream(tmp), hash);
    const actual = hash.digest("hex");
    if (actual !== artifact.sha256) {
      throw new Error(
        `runtime checksum mismatch (expected ${artifact.sha256}, got ${actual}). Refusing to install.`,
      );
    }
    rmSync(installDir, { recursive: true, force: true });
    mkdirSync(installDir, { recursive: true, mode: 0o700 });
    try {
      chmodSync(installDir, 0o700);
    } catch {
      // best-effort on platforms without chmod
    }
    const res = spawnSync("tar", ["-xzf", tmp, "-C", installDir], {
      stdio: ["ignore", "ignore", "pipe"],
    });
    assertTarExtractionSucceeded(res);
    if (!existsSync(binPath)) {
      throw new Error(`runtime extracted but entry missing: ${binPath}`);
    }
    writeFileSync(marker, artifact.sha256);
    return { binPath, downloaded: true };
  } finally {
    rmSync(tmp, { force: true });
  }
}

// --- run ------------------------------------------------------------------------

export interface UpdateCliDeps {
  readonly env?: Readonly<Record<string, string | undefined>>;
  readonly stdout?: (line: string) => void;
  readonly stderr?: (line: string) => void;
  readonly fetchImpl?: typeof fetch;
  readonly currentVersion?: string;
  readonly platform?: NodeJS.Platform;
  readonly arch?: string;
  readonly userHome?: string;
}

export async function runAgenCUpdateCli(
  command: AgenCUpdateCliCommand,
  deps: UpdateCliDeps = {},
): Promise<number> {
  const stdout =
    deps.stdout ?? ((line: string) => process.stdout.write(`${line}\n`));
  const stderr =
    deps.stderr ?? ((line: string) => process.stderr.write(`${line}\n`));
  if (command.kind === "help") {
    stdout(command.text);
    return 0;
  }
  if (command.kind === "error") {
    stderr(`agenc: ${command.message}`);
    return 1;
  }

  const env = deps.env ?? process.env;
  const currentVersion = deps.currentVersion ?? VERSION;
  const manifestUrl = resolveUpdateManifestUrl({ ...command, env });

  let manifest: RuntimeManifest;
  let artifact: RuntimeManifestArtifact;
  try {
    manifest = await fetchRuntimeManifest(manifestUrl, deps.fetchImpl);
    artifact = selectUpdateArtifact(
      manifest,
      updatePlatformSlug(deps.platform, deps.arch),
    );
  } catch (error) {
    stderr(`agenc: update failed: ${error instanceof Error ? error.message : String(error)}`);
    return 1;
  }

  const latestVersion = manifest.runtimeVersion;
  // Unpinned updates only when remote is semver-greater (todo-121).
  // --pin installs that exact release even if lower.
  const pinned =
    "pin" in command &&
    typeof (command as { pin?: string }).pin === "string" &&
    (command as { pin: string }).pin.length > 0;
  let updateAvailable = false;
  try {
    updateAvailable = pinned
      ? latestVersion !== currentVersion
      : semverGt(latestVersion, currentVersion);
  } catch {
    updateAvailable = latestVersion !== currentVersion;
  }

  if (!updateAvailable) {
    if (command.json) {
      stdout(
        JSON.stringify({
          currentVersion,
          latestVersion,
          updateAvailable: false,
        }),
      );
    } else if (
      !pinned &&
      latestVersion !== currentVersion
    ) {
      stdout(
        `agenc ${currentVersion} is newer than or equal to remote ${latestVersion}; not downgrading. Use --pin ${latestVersion} to force.`,
      );
    } else {
      stdout(`agenc ${currentVersion} is up to date.`);
    }
    return 0;
  }

  if (command.check) {
    if (command.json) {
      stdout(JSON.stringify({ currentVersion, latestVersion, updateAvailable: true }));
    } else {
      stdout(
        `Update available: ${currentVersion} -> ${latestVersion}. Run 'agenc update' to install.`,
      );
    }
    return 0;
  }

  // Locate the wrapper BEFORE downloading: without one there is nothing to
  // repoint, and downloading a runtime nothing will launch helps nobody.
  let wrappers: InstallShWrapper[];
  if (command.wrapper !== undefined) {
    const explicit = parseInstallShWrapper(command.wrapper);
    if (explicit === null) {
      stderr(
        `agenc: ${command.wrapper} is not a wrapper generated by install.sh; refusing to rewrite it`,
      );
      return 1;
    }
    wrappers = [explicit];
  } else {
    wrappers = findInstallShWrappers({ env, userHome: deps.userHome });
  }
  if (wrappers.length === 0) {
    stderr("agenc: no install.sh wrapper found on PATH.");
    stderr(
      "agenc: if you installed via npm, update with: npm install -g @tetsuo-ai/agenc@latest",
    );
    stderr(
      "agenc: otherwise re-run the installer (curl -fsSL https://get.agenc.ag/install.sh | sh)",
    );
    stderr("agenc: or pass --wrapper <path> to the generated wrapper script.");
    return 1;
  }

  const agencHome = resolveAgencHome(env);
  let result: InstallRuntimeResult;
  try {
    result = await installRuntimeFromManifest({
      manifest,
      artifact,
      agencHome,
      ...(deps.fetchImpl !== undefined ? { fetchImpl: deps.fetchImpl } : {}),
    });
  } catch (error) {
    stderr(`agenc: update failed: ${error instanceof Error ? error.message : String(error)}`);
    return 1;
  }

  for (const wrapper of wrappers) {
    try {
      rewriteWrapper(wrapper, result.binPath);
    } catch (error) {
      stderr(
        `agenc: installed runtime ${latestVersion} but could not rewrite ${wrapper.path}: ${error instanceof Error ? error.message : String(error)}`,
      );
      return 1;
    }
  }

  if (command.json) {
    stdout(
      JSON.stringify({
        currentVersion,
        latestVersion,
        updateAvailable: true,
        installed: true,
        downloaded: result.downloaded,
        runtimeBin: result.binPath,
        wrappers: wrappers.map((w) => w.path),
      }),
    );
  } else {
    stdout(
      `Updated agenc ${currentVersion} -> ${latestVersion} (${result.downloaded ? "downloaded" : "already present"}, sha256 verified).`,
    );
    for (const wrapper of wrappers) {
      stdout(`  wrapper repointed: ${wrapper.path}`);
    }
    stdout("Restart the daemon to pick it up: agenc daemon restart");
  }
  return 0;
}
