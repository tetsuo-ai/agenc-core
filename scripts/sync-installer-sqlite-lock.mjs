#!/usr/bin/env node

import { readFileSync, writeFileSync } from "node:fs";
import { dirname, join, resolve } from "node:path";
import { fileURLToPath } from "node:url";

const repoRoot = resolve(dirname(fileURLToPath(import.meta.url)), "..");
const sqliteSourcePath = join(repoRoot, "packages/agenc/lib/sqlite-lock.mjs");
const activationIdentitySourcePath = join(
  repoRoot,
  "packages/agenc/lib/activation-lock-identity.mjs",
);
const generatedWrapperSourcePath = join(
  repoRoot,
  "packages/agenc/lib/generated-wrapper.mjs",
);
const installerPaths = [
  join(repoRoot, "scripts/install/install.sh"),
  join(repoRoot, "scripts/install/install.ps1"),
];
const sqliteStartMarker = "// BEGIN GENERATED AGENC SQLITE LOCK MODULE";
const sqliteEndMarker = "// END GENERATED AGENC SQLITE LOCK MODULE";
const sqliteLegacyStart = 'const { DatabaseSync } = require("node:sqlite");';
const sqliteLegacyEnd = `function acquireLock(path, options) {
  return acquireLocks([path], options);
}`;
const identityStartMarker = "// BEGIN GENERATED AGENC ACTIVATION LOCK IDENTITY MODULE";
const identityEndMarker = "// END GENERATED AGENC ACTIVATION LOCK IDENTITY MODULE";
const identityLegacyStart = "function existingAgenCHomeIdentity(requested) {";
const identityLegacyEnd = "function activationTestDelay(name) {";
const wrapperStartMarker = "// BEGIN GENERATED AGENC WRAPPER CONTRACT MODULE";
const wrapperEndMarker = "// END GENERATED AGENC WRAPPER CONTRACT MODULE";
const wrapperLegacyStart = "function isGeneratedWrapperContent(content) {";

function generatedSqliteBlock(source) {
  const encoded = Buffer.from(source, "utf8").toString("base64");
  return `${sqliteStartMarker}
// Generated by scripts/sync-installer-sqlite-lock.mjs from the canonical
// launcher module. Do not edit this embedded payload by hand.
const AGENC_SQLITE_LOCK_SOURCE_BASE64 = ${JSON.stringify(encoded)};
let sqliteLockModulePromise;
function loadSqliteLockModule() {
  sqliteLockModulePromise ??= import(
    \`data:text/javascript;base64,\${AGENC_SQLITE_LOCK_SOURCE_BASE64}\`,
  );
  return sqliteLockModulePromise;
}
${sqliteEndMarker}`;
}

function generatedActivationIdentityBlock(source) {
  const encoded = Buffer.from(source, "utf8").toString("base64");
  return `${identityStartMarker}
// Generated by scripts/sync-installer-sqlite-lock.mjs from the canonical
// launcher module. Do not edit this embedded payload by hand.
const AGENC_ACTIVATION_LOCK_IDENTITY_SOURCE_BASE64 = ${JSON.stringify(encoded)};
let activationLockIdentityModulePromise;
function loadActivationLockIdentityModule() {
  activationLockIdentityModulePromise ??= import(
    \`data:text/javascript;base64,\${AGENC_ACTIVATION_LOCK_IDENTITY_SOURCE_BASE64}\`,
  );
  return activationLockIdentityModulePromise;
}
${identityEndMarker}`;
}

function generatedWrapperBlock(source) {
  const encoded = Buffer.from(source, "utf8").toString("base64");
  return `${wrapperStartMarker}
// Generated by scripts/sync-installer-sqlite-lock.mjs from the canonical
// launcher module. Do not edit this embedded payload by hand.
const AGENC_GENERATED_WRAPPER_SOURCE_BASE64 = ${JSON.stringify(encoded)};
let generatedWrapperModulePromise;
function loadGeneratedWrapperModule() {
  generatedWrapperModulePromise ??= import(
    \`data:text/javascript;base64,\${AGENC_GENERATED_WRAPPER_SOURCE_BASE64}\`,
  );
  return generatedWrapperModulePromise;
}
${wrapperEndMarker}`;
}

function replaceGeneratedBlock(
  input,
  { startMarker, endMarker, legacyStart, legacyEnd, retainLegacyEnd = false },
  replacement,
  path,
) {
  const normalizeBoundary = (suffix) =>
    `${replacement}${suffix.replace(/^(?:\r?\n)*/u, "\n\n")}`;
  const markerCount = (marker) => input.split(marker).length - 1;
  const generatedStart = input.indexOf(startMarker);
  if (generatedStart !== -1) {
    const generatedEnd = input.indexOf(endMarker, generatedStart);
    if (
      generatedEnd === -1 ||
      markerCount(startMarker) !== 1 ||
      markerCount(endMarker) !== 1
    ) {
      throw new Error(`invalid generated SQLite lock markers: ${path}`);
    }
    return input.slice(0, generatedStart) + normalizeBoundary(
      input.slice(generatedEnd + endMarker.length),
    );
  }
  if (markerCount(endMarker) !== 0) {
    throw new Error(`invalid generated SQLite lock markers: ${path}`);
  }

  const start = input.indexOf(legacyStart);
  const end = input.indexOf(legacyEnd, start);
  if (start === -1 || end === -1 || input.indexOf(legacyStart, start + 1) !== -1) {
    throw new Error(`cannot locate the legacy generated block: ${path}`);
  }
  return input.slice(0, start) + normalizeBoundary(
    input.slice(retainLegacyEnd ? end : end + legacyEnd.length),
  );
}

const args = new Set(process.argv.slice(2));
for (const arg of args) {
  if (arg !== "--check") throw new Error(`unknown argument: ${arg}`);
}
const sqliteReplacement = generatedSqliteBlock(readFileSync(sqliteSourcePath, "utf8"));
const identityReplacement = generatedActivationIdentityBlock(
  readFileSync(activationIdentitySourcePath, "utf8"),
);
const wrapperReplacement = generatedWrapperBlock(
  readFileSync(generatedWrapperSourcePath, "utf8"),
);
let stale = false;
for (const path of installerPaths) {
  const current = readFileSync(path, "utf8");
  const withSqlite = replaceGeneratedBlock(
    current,
    {
      startMarker: sqliteStartMarker,
      endMarker: sqliteEndMarker,
      legacyStart: sqliteLegacyStart,
      legacyEnd: sqliteLegacyEnd,
    },
    sqliteReplacement,
    path,
  );
  const withIdentity = replaceGeneratedBlock(
    withSqlite,
    {
      startMarker: identityStartMarker,
      endMarker: identityEndMarker,
      legacyStart: identityLegacyStart,
      legacyEnd: identityLegacyEnd,
      retainLegacyEnd: true,
    },
    identityReplacement,
    path,
  );
  const expected = replaceGeneratedBlock(
    withIdentity,
    {
      startMarker: wrapperStartMarker,
      endMarker: wrapperEndMarker,
      legacyStart: wrapperLegacyStart,
      legacyEnd: wrapperLegacyStart,
      retainLegacyEnd: true,
    },
    wrapperReplacement,
    path,
  );
  if (current === expected) continue;
  stale = true;
  if (!args.has("--check")) writeFileSync(path, expected);
  else process.stderr.write(`stale embedded installer lock modules: ${path}\n`);
}
if (args.has("--check") && stale) process.exitCode = 1;
